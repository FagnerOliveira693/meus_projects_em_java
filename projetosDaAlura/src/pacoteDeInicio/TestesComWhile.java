package pacoteDeInicio;

public class TestesComWhile {

	public static void main(String[] args) {
		int i = 0;
		while (i <= 30) {
			
			
			
			
			System.out.println(i);
			i++;
			if (i == 10) {
				System.out.println("Ops... Você chegou no seu limite máximo! \n\tNão pode mais utrapassar... ");
				break;
			}
			
			
		}
		
		
		
		
	}
}
